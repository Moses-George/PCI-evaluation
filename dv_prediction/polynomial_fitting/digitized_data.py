classes = ["alligator", "linear", "pothole"]


def plotCurve(class_name, name, xrange, data_points):
    x1 = data_points["low"]["density"]
    y1 = data_points["low"]["dv"]

    x2 = data_points["medium"]["density"]
    y2 = data_points["medium"]["dv"]

    x3 = data_points["high"]["density"]
    y3 = data_points["high"]["dv"]

    plt.figure(figsize=(8, 6))

    plt.plot(x1, y1, label="Low", color="blue", marker="o")
    plt.plot(x2, y2, label="Medium", color="red", marker="s")
    plt.plot(x3, y3, label="High", color="green", marker="^")

    plt.xscale("log")
    plt.xlim(xrange[0], xrange[1])
    plt.ylim(0, 100)

    plt.yticks(np.arange(0, 101, 10))

    plt.xlabel("X axis (Log Scale)")
    plt.ylabel("Y axis (Linear Scale)")
    plt.grid(True, which="both", ls="--", color="0.65")
    plt.title(name)
    plt.legend()

    plt.show()


if __name__ == "__main__":

    for distress in DISTRESS_CURVES:
        class_name = distress["class"]
        xrange = distress["xrange"]
        data_points = distress["data_points"]
        name = distress["name"]
        plotCurve(class_name, name, xrange, data_points)
